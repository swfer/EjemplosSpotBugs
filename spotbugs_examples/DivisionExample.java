/**
 * Ejemplo división 
 * 
 * Se desea dividir el contenido de la variable a entre b 
 * y mostrar el resultado por consola.
 *
 */
public class DivisionExample {

	public static void example() {
		int a = 1;
		int b = 3; 
		double division =  a / b;
		System.out.println("Ejemplo división: " + division);
	}
	
}
